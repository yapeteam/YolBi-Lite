    public void roundedRectangle(double x, double y, double width, double height, double radius, Color color) {
        start();

        if (color != null) {
            ColorManager.glColor(color);
        }

        // Draw the center rectangle
        GL11.glBegin(GL11.GL_QUADS);
        GL11.glVertex2d(x + radius, y);
        GL11.glVertex2d(x + width - radius, y);
        GL11.glVertex2d(x + width - radius, y + height);
        GL11.glVertex2d(x + radius, y + height);
        GL11.glEnd();

        // Draw the side rectangles
        GL11.glBegin(GL11.GL_QUADS);
        GL11.glVertex2d(x, y + radius);
        GL11.glVertex2d(x + radius, y + radius);
        GL11.glVertex2d(x + radius, y + height - radius);
        GL11.glVertex2d(x, y + height - radius);

        GL11.glVertex2d(x + width - radius, y + radius);
        GL11.glVertex2d(x + width, y + radius);
        GL11.glVertex2d(x + width, y + height - radius);
        GL11.glVertex2d(x + width - radius, y + height - radius);
        GL11.glEnd();

        // Draw the corner circles
        GL11.glBegin(GL11.GL_TRIANGLE_FAN);
        for (int i = 0; i <= 90; i += 3) {
            double angle = Math.toRadians(i);
            GL11.glVertex2d(x + radius + Math.sin(angle) * radius, y + radius - Math.cos(angle) * radius);
        }
        GL11.glEnd();

        GL11.glBegin(GL11.GL_TRIANGLE_FAN);
        for (int i = 0; i <= 90; i += 3) {
            double angle = Math.toRadians(i);
            GL11.glVertex2d(x + width - radius + Math.sin(angle) * radius, y + radius - Math.cos(angle) * radius);
        }
        GL11.glEnd();

        GL11.glBegin(GL11.GL_TRIANGLE_FAN);
        for (int i = 0; i <= 90; i += 3) {
            double angle = Math.toRadians(i);
            GL11.glVertex2d(x + width - radius + Math.sin(angle) * radius, y + height - radius + Math.cos(angle) * radius);
        }
        GL11.glEnd();

        GL11.glBegin(GL11.GL_TRIANGLE_FAN);
        for (int i = 0; i <= 90; i += 3) {
            double angle = Math.toRadians(i);
            GL11.glVertex2d(x + radius + Math.sin(angle) * radius, y + height - radius + Math.cos(angle) * radius);
        }
        GL11.glEnd();

        stop();
    }

    public void dropShadow(final int loops, final double x, final double y, final double width, final double height, final double opacity, final double edgeRadius) {
        GlStateManager.alphaFunc(516, 0);
        GlStateManager.enableBlend();
        GlStateManager.enableAlpha();

        for (float margin = 0; margin <= loops / 2f; margin += 0.5f) {
            roundedRectangle(x - margin / 2f, y - margin / 2f,
                    width + margin, height + margin, edgeRadius,
                    new Color(0, 0, 0, (int) Math.max(0.5f, (opacity - margin * 1.2) / 5.5f)));
        }
    }

